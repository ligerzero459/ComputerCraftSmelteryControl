function getColor(color)

  if strlower(color) == "black" then
    return colors.black
  elseif strlower(color) == "red" then
    return colors.red
  elseif strlower(color) == "green" then
    return colors.green
  elseif strlower(color) == "brown" then
    return colors.brown
  elseif strlower(color) == "blue" then
    return colors.blue
  elseif strlower(color) == "purple" then
    return colors.purple
  elseif strlower(color) == "cyan" then
    return colors.cyan
  elseif strlower(color) == "light gray" then
    return colors.lightGray
  elseif strlower(color) == "gray" then
    return colors.gray
  elseif strlower(color) == "pink" then
    return colors.pink
  elseif strlower(color) == "lime" then
    return colors.lime
  elseif strlower(color) == "yellow" then
    return colors.yellow
  elseif strlower(color) == "light blue" then
    return colors.lightBlue
  elseif strlower(color) == "magenta" then
    return colors.magenta
  elseif strlower(color) == "orange" then
    return colors.orange
  elseif strlower(color) == "white" then
    return colors.white
  end

end
